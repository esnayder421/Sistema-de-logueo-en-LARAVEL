    {
        $usuario = $request->get('usuario');
        $clave = $request->get('clave');
        $ingreso = Login::where("usuario" ,$usuario)->first();
        if(empty($ingreso)){
            $mensaje = "'danger', 'usuario o clave incorrecto.'";
            return view('index')->with(['mensaje'=>$mensaje]);
        }else if($ingreso->rol == 'administrador'){
